CREATE OR REPLACE FUNCTION generate_business_id(p_prefix text)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_next integer;
BEGIN
  INSERT INTO id_sequences (prefix, next_value)
  VALUES (p_prefix, 1)
  ON CONFLICT (prefix)
  DO UPDATE SET next_value = id_sequences.next_value + 1
  RETURNING next_value INTO v_next;
  RETURN p_prefix || '-' || lpad(v_next::text, 6, '0');
END;
$$;

