CREATE OR REPLACE FUNCTION enforce_single_settings_row()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF (SELECT count(*) FROM settings) >= 1 AND TG_OP = 'INSERT' THEN
    RAISE EXCEPTION 'Settings table can only contain one row';
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS settings_single_row ON settings;
CREATE TRIGGER settings_single_row
  BEFORE INSERT ON settings
  FOR EACH ROW EXECUTE FUNCTION enforce_single_settings_row();

