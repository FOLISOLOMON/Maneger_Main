/*
# Veloura Manager V2 — Core Schema

1. Purpose
   Builds the complete relational data model for Veloura Manager V2, a perfume
   business financial management system. The Inventory Batch is the central
   entity: products, sales, batch expenses, and wallet allocations all attach
   to a batch. Customers and suppliers are business-level entities.

2. Tables created (11 core + 1 helper)
   - settings            : single-row business configuration
   - suppliers           : vendors that supply inventory batches
   - inventory_batches   : the core entity — one stock purchase cycle
   - products            : perfumes, each belonging to exactly one batch
   - customers           : buyers (business-level, not batch-scoped)
   - sales               : one record per sale transaction
   - expenses            : unified batch + business expenses
   - wallet_transactions : append-only ledger for Needs/Savings/Growth wallets
   - notifications       : system + business alerts
   - activity_logs       : audit trail for every important action
   - id_sequences        : counter table powering human-readable IDs (BAT-000001 etc.)

3. ID strategy
   Human-readable business IDs (BAT-000001, PRD-000001, SAL-000001, ...) are
   generated by a `generate_business_id(prefix)` PL/pgSQL function backed by
   the `id_sequences` table. These are stored in `<entity>_code` columns and
   are immutable after creation. UUIDs remain the primary keys for joins.

4. Computed vs stored values
   Per spec section 5.18, derived values (wallet balances, customer lifetime
   value, dashboard KPIs, batch completion %) are NOT stored. They are
   computed in the calculation services from source rows. Batch aggregates
   (gross_revenue, gross_profit, net_profit, roi, completion_percentage,
   remaining_stock) ARE stored because they are historically meaningful and
   expensive to recompute across all sales.

5. Triggers
   - `set_updated_at`    : maintains updated_at on every update.
   - `enforce_single_settings_row` : guarantees the settings table never has
     more than one row.

6. Security (RLS)
   This is a single-tenant, no-auth app (Version 2 = single business owner,
   no sign-in screen per spec 3.2). All policies use `TO anon, authenticated`
   with `USING (true)` / `WITH CHECK (true)` because the data is intentionally
   shared within the single business. This matches the bolt-database guidance
   for no-auth single-tenant apps.

7. Data integrity
   - Foreign keys enforce: product->batch, sale->product+customer, batch->supplier,
     batch expense->batch, wallet_transaction->batch (nullable for direct
     transfers/withdrawals).
   - `CHECK` constraints enforce valid enum values for status / type columns.
   - Settings table locked to a single row.
*/

